#include "3-calc.h"

/**
 * op_add - a function get the sum
 *
 * @a: first input
 * @b:second input
 *
 * Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function get the sub
 *
 * @a: first input
 * @b: second input
 *
 * Return: the sub
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function get the mul
 *
 * @a: first input
 * @b: second input
 *
 * Return: the mul
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function get the div
 *
 * @a: first input
 * @b: second input
 *
 * Return: the div
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a funcction get the mod
 *
 * @a: first input
 * @b: second input
 *
 * Return: the mod
*/
int op_mod(int a, int b)
{
	return (a % b);
}
